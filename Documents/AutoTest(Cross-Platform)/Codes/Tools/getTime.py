import datetime


class getTime:
    toDay = ''
    toHour=''
    toMinuter=''
    toSecond=''
    date = ''

    def __init__(self):
        self.date = datetime.datetime.now()

    def getToDay(self):
        self.toDay = datetime.datetime.now().strftime("%Y-%m-%d")
        return self.toDay

    def getToHour(self):
        self.toHour = datetime.datetime.now().strftime("%Y-%m-%d %H")
        return self.toHour

    def getToMinuter(self):
        self.toMinuter = datetime.datetime.now().strftime("%Y-%m-%d %H-%M")
        return self.toMinuter

    def getToSecond(self):
        self.toSecond = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        return self.toSecond
